#!/usr/bin/env node
// Generates src/fonts-embed.css with the app's web fonts inlined as base64
// woff2 data URIs, so the downloaded single-file MilCalc.html renders with the
// correct typography even with no internet connection.
//
// Run once (and re-run whenever the font set changes):  npm run fonts:embed
// The generated CSS file is committed to the repo; the build does NOT fetch
// fonts, so release builds stay hermetic and offline-capable.
//
// We only keep the `latin` subset of each face to keep the bundle reasonable.

const fs = require("fs");
const path = require("path");

// The exact families/weights the app uses (mirrors the old Google Fonts @import
// that used to live in src/App.jsx).
const FAMILIES = [
  "Bebas+Neue",
  "Barlow+Condensed:wght@600;700",
  "Libre+Baskerville:wght@700",
  "Barlow:wght@400;500;600;700",
  "IBM+Plex+Mono:wght@400;500;600",
  "Rajdhani:wght@500;600;700",
  "Inter:wght@400;500;600",
];

const CSS_URL =
  "https://fonts.googleapis.com/css2?" +
  FAMILIES.map((f) => "family=" + f).join("&") +
  "&display=swap";

// A modern Chrome UA makes Google Fonts serve woff2 (smallest, well-supported).
const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 " +
  "(KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36";

async function main() {
  console.log("Fetching font CSS:\n  " + CSS_URL);
  const cssRes = await fetch(CSS_URL, { headers: { "User-Agent": UA } });
  if (!cssRes.ok) throw new Error("Font CSS fetch failed: " + cssRes.status);
  const css = await cssRes.text();

  // Each face is preceded by a `/* <subset> */` comment.
  const re = /\/\*\s*([\w-]+)\s*\*\/\s*(@font-face\s*\{[\s\S]*?\})/g;
  const out = [];
  let m;
  let kept = 0;
  while ((m = re.exec(css)) !== null) {
    const subset = m[1];
    const block = m[2];
    if (subset !== "latin") continue;
    const urlMatch = block.match(/url\((https:\/\/[^)]+\.woff2)\)/);
    if (!urlMatch) continue;
    const fontUrl = urlMatch[1];
    const fontRes = await fetch(fontUrl, { headers: { "User-Agent": UA } });
    if (!fontRes.ok) throw new Error("Font fetch failed: " + fontUrl);
    const buf = Buffer.from(await fontRes.arrayBuffer());
    const dataUri = "data:font/woff2;base64," + buf.toString("base64");
    const inlined = block.replace(/url\(https:\/\/[^)]+\.woff2\)/, `url(${dataUri})`);
    const fam = (block.match(/font-family:\s*'([^']+)'/) || [])[1] || "?";
    const wght = (block.match(/font-weight:\s*(\d+)/) || [])[1] || "?";
    console.log(`  embedded ${fam} ${wght} (${(buf.length / 1024).toFixed(1)} KB)`);
    out.push(inlined);
    kept++;
  }

  if (!kept) throw new Error("No latin @font-face blocks found — aborting.");

  const header =
    "/* AUTO-GENERATED by scripts/generate-fonts-embed.js — do not edit by hand.\n" +
    " * Web fonts inlined as base64 woff2 so the downloaded MilCalc.html works\n" +
    " * fully offline. Regenerate with: npm run fonts:embed */\n\n";

  const dest = path.join(__dirname, "..", "src", "fonts-embed.css");
  fs.writeFileSync(dest, header + out.join("\n\n") + "\n");
  console.log(`\nWrote ${kept} faces -> ${dest}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
